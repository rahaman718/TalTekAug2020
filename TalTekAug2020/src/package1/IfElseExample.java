package package1;

public class IfElseExample {

	public static void main(String[] args) {


		int pt =10;
		
		int st1=15;
		int st2=10;
		int st3=9;
		
		if(pt==st1){
			
			System.out.println("Buying from Store1 ");
		}
		else if (pt==st2){
			
			System.out.println("Buying from Store2 ");
		}
		else if (pt==st3){
			
			System.out.println("Buying from Store3 ");
		}
		else{
			
			System.out.println("Do not have enough money");
		}
		
		

	}

}
